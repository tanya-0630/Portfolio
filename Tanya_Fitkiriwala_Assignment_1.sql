-- Join query 1
Select rec_ingredient_id, a.recipe_id, amount, a.ingredient_id, ingred_name,
rec_title, c.category_id, d.category_name, recipe_desc, prep_time, cook_time, servings,
difficulty,directions
 from rec_ingredients as a
left join ingredients as b on a.ingredient_id = b.ingredient_id
right join (select * from recipe_main where recipe_id >2) as c 
on a.recipe_id = c.recipe_id
left join categories as d on c.category_id = d.category_id
order by category_name, a.ingredient_id;

-- Join Query 2
Select rec_title, d.category_name, ingred_name, amount
 from rec_ingredients as a
left join ingredients as b on a.ingredient_id = b.ingredient_id
right join (select * from recipe_main where recipe_id >2) as c 
on a.recipe_id = c.recipe_id
left join categories as d on c.category_id = d.category_id
order by category_name desc, rec_title, ingred_name desc;

